package com.lol.hgl.dto;

public class kindstoreDto {
	
	private int kindNum;
	private String kindDo;
	private String kindCity;
	private String kindType;
	private String kindStorename;
	private String kindPhone;
	private String kindAddr;
	private String kindDelivery;
	private String kindParking;
	private String kindMenu;
	private String kindPrice;
	
	
	public kindstoreDto() {
		
	}
	
	public kindstoreDto(int kindNum, String kindDo, String kindCity, String kindType, String kindStorename,
			String kindPhone, String kindAddr, String kindDelivery, String kindParking, String kindMenu,
			String kindPrice) {
		this.kindNum = kindNum;
		this.kindDo = kindDo;
		this.kindCity = kindCity;
		this.kindType = kindType;
		this.kindStorename = kindStorename;
		this.kindPhone = kindPhone;
		this.kindAddr = kindAddr;
		this.kindDelivery = kindDelivery;
		this.kindParking = kindParking;
		this.kindMenu = kindMenu;
		this.kindPrice = kindPrice;
	}

	public int getKindNum() {
		return kindNum;
	}

	public void setKindNum(int kindNum) {
		this.kindNum = kindNum;
	}

	public String getKindDo() {
		return kindDo;
	}

	public void setKindDo(String kindDo) {
		this.kindDo = kindDo;
	}

	public String getKindCity() {
		return kindCity;
	}

	public void setKindCity(String kindCity) {
		this.kindCity = kindCity;
	}

	public String getKindType() {
		return kindType;
	}

	public void setKindType(String kindType) {
		this.kindType = kindType;
	}

	public String getKindStorename() {
		return kindStorename;
	}

	public void setKindStorename(String kindStorename) {
		this.kindStorename = kindStorename;
	}

	public String getKindPhone() {
		return kindPhone;
	}

	public void setKindPhone(String kindPhone) {
		this.kindPhone = kindPhone;
	}

	public String getKindAddr() {
		return kindAddr;
	}

	public void setKindAddr(String kindAddr) {
		this.kindAddr = kindAddr;
	}

	public String getKindDelivery() {
		return kindDelivery;
	}

	public void setKindDelivery(String kindDelivery) {
		this.kindDelivery = kindDelivery;
	}

	public String getKindParking() {
		return kindParking;
	}

	public void setKindParking(String kindParking) {
		this.kindParking = kindParking;
	}

	public String getKindMenu() {
		return kindMenu;
	}

	public void setKindMenu(String kindMenu) {
		this.kindMenu = kindMenu;
	}

	public String getKindPrice() {
		return kindPrice;
	}

	public void setKindPrice(String kindPrice) {
		this.kindPrice = kindPrice;
	}
	
	
	
	

}
