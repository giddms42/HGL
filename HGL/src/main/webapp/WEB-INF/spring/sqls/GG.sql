CREATE SEQUENCE GGCMNO_SEQ;

--GGCM TABLE SQL
DROP TABLE GGCM;
CREATE TABLE GGCM
(
    GGNO          NUMBER            REFERENCES  GG(GGNO) ON DELETE CASCADE,
    GGCMNO        NUMBER            NOT NULL, 
    GGCMWRITER    VARCHAR2(100)     NOT NULL, 
    GGCMCONT      VARCHAR2(1000)    NOT NULL, 
    GGCMDATE      DATE              NOT NULL, 
    CONSTRAINT GGCM_PK PRIMARY KEY (GGCMNO)
);

