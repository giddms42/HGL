--GG SEQUENCE SQL
DROP SEQUENCE GGNO_SEQ;
CREATE SEQUENCE GGNO_SEQ;

--GG TABLE SQL
DROP TABLE GG;
CREATE TABLE GG
(
    GGNO         NUMBER            NOT NULL,
    GGROWNUM     NUMBER			   NULL,
    GGWRITER     VARCHAR2(100)     NOT NULL, 
    GGTITLE      VARCHAR2(200)     NOT NULL, 
    GGCONT       VARCHAR2(4000)    NOT NULL, 
    GGREADCNT    NUMBER            NOT NULL, 
    GGDATE       DATE              NOT NULL, 
    CONSTRAINT GG_PK PRIMARY KEY (GGNO)
);

